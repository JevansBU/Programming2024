def celsius_to_fahrenheit(celsius):
   # complete your function implementation... 

   output = (celsius * 9/5)
   output += 32

   return output

#User input and print
celsius = 20
print(f"{celsius_to_fahrenheit(celsius)} Fahrenheit!")
celsius = 24
print(f"{celsius_to_fahrenheit(celsius)} Fahrenheit!")


# 20 Celsius in Fahrenheit = 68.0
